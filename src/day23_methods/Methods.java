package day23_methods;
/* Access-modifier specifier return-type methodName{}{
                 statements;
 */
public class Methods {
    public static void main(String[] args) {
        printHello5Times();
        System.out.println();
    }

    public static void printHello5Times() {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");
        }




    }


}