package day02_Variables;

public class EscapeSequences {
    public static void main (String[] args) {
        System.out.println("Hello");
        System.out.println("\t\t\t\t\tHello");
        System.out.println("\t\tHEllo");

          //  \t means a pragraph space
        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println("\nMy\nName\nIs\nHilal");


        //    \n means a new line

        //print: My favorite TV show is "Game of the stones"
        System.out.println("My favorite show is \"Game of the stones\"");
        System.out.println("My favorite book is \"The Little Prens\"");

        //    \" prints double quote on the console
        //     \\prints single slash -\

        System.out.println("\\");
        System.out.println("/");

        System.out.println("My favorite book is 'Java'");
        System.out.println("My favorite book is \'Java\'");

        //

    }
}
